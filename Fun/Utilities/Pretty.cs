    {

      if (color.StartsWith("#"))
      {
        color = color[1..];
      }

      var r = Convert.ToUInt32(color.Substring(0, 2), 16);
      var g = Convert.ToUInt32(color.Substring(2, 2), 16);
      var b = Convert.ToUInt32(color.Substring(4, 2), 16);

      Console.Write($"\u001B[38;2;{r};{g};{b}m{message}\u001B[0m\n");
    }


